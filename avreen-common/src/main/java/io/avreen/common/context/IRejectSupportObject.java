package io.avreen.common.context;

/**
 * The interface Reject support object.
 */
public interface IRejectSupportObject {

    /**
     * Is reject boolean.
     *
     * @return the boolean
     */
    boolean isReject();

    /**
     * Gets reject code.
     *
     * @return the reject code
     */
    Integer getRejectCode();

    /**
     * Sets reject code.
     *
     * @param rejectCode the reject code
     */
    void setRejectCode(int rejectCode);

}
